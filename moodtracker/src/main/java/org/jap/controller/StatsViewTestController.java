package org.jap.controller;

import javafx.application.Application;
import javafx.scene.Group;
import javafx.scene.Scene;
import javafx.scene.chart.CategoryAxis;
import javafx.stage.Stage;
import javafx.scene.chart.LineChart;
import javafx.scene.chart.NumberAxis;
import javafx.scene.chart.XYChart;

public class StatsViewTestController {
//    @Override
    public void start(Stage stage) {
        //Defining the x axis
        CategoryAxis xAxis = new CategoryAxis();
        xAxis.setLabel("Years");

        //Defining the y axis
        NumberAxis yAxis = new NumberAxis   (0, 350, 50);
        yAxis.setLabel("No.of schools");

        //Creating the line chart
        LineChart<String, Number> lineChart = new LineChart<String, Number>(xAxis, yAxis);

        //Prepare XYChart.Series objects by setting data
        XYChart.Series series = new XYChart.Series();
        series.setName("No of schools in an year");

        series.getData().add(new XYChart.Data<String, Number>("Jul", 15));
        series.getData().add(new XYChart.Data<String, Number>("1980", 30));
        series.getData().add(new XYChart.Data<String, Number>("1990", 60));
        series.getData().add(new XYChart.Data<String, Number>("2000", 120));
        series.getData().add(new XYChart.Data<String, Number>("2013", 240));
        series.getData().add(new XYChart.Data<String, Number>("2014", 300));

        //Setting the data to Line chart
        lineChart.getData().add(series);

        //Creating a Group object
        Group root = new Group(lineChart);

        //Creating a scene object
        Scene scene = new Scene(root, 600, 400);

        //Setting title to the Stage
        stage.setTitle("Line Chart");

        //Adding scene to the stage
        stage.setScene(scene);

        //Displaying the contents of the stage
        stage.show();
    }


    }
